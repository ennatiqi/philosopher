#include "ft_philo.h"

int	ft_atoi(char *n)
{
	int	i;
	int	neg;

	i = 0;
	neg = 1;
	while ((*n >= 9 && *n <= 13) || *n == ' ')
		n++;
	if (*n == '+' || *n == '-')
	{
		if (*n == '-')
			neg *= -1;
		n++;
	}
	while (*n >= '0' && *n <= '9')
	{
		i = (i * 10) + (*n - '0');
		n++;
	}
	return (i * neg);
}
